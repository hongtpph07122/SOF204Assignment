    public void getData() {
        try {
            listQ = qDAO.findAll();
            if (listQ == null) {
                JOptionPane.showMessageDialog(this, "LỖI LOAD DATA");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "LỖI LOAD DATA");
        }
    }

    public void filltotable() {
        int gg = 1;
         model.setRowCount(0);
        for (Questions x : listQ) {
            model.addRow(new Object[]{gg, x.getQ(), x.getAa(), x.getAb(), x.getAc(), x.getAd(), x.getAnswer()});
            gg++;
        }
        if (listQ.size() > 0) {
            index = 0;
            tbl_danhsach.setRowSelectionInterval(index, index);
            this.showDetail();
        }
    }

    public void showDetail() {
        tbl_danhsach.setRowSelectionInterval(index, index);
        Questions q = listQ.get(index);
        txtA.setText(q.getAa());
        txtB.setText(q.getAb());
        txtC.setText(q.getAc());
        txtD.setText(q.getAd());
        txtDe.setText(q.getQ());
        cbo.setSelectedItem(q.getAnswer());
    }

    public void clearForm() {
        txtA.setText("");
        txtB.setText("");
        txtC.setText("");
        txtD.setText("");
        txtDe.setText("");
    }

    public boolean validateForm() {
        if (txtDe.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "ĐỀ BÀI KHÔNG ĐƯỢC BỎ TRỐNG");
            return false;
        } else {
            if (txtA.getText().equals("") || txtB.getText().equals("") || txtC.getText().equals("") || txtD.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "CÁC ĐÁP ÁN KHÔNG ĐƯỢC BỎ TRỐNG");
                return false;
            } else {
                return true;
            }

        }
    }
