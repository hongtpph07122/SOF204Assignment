    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            index = tbl_danhsach.getSelectedRow();
            int input = JOptionPane.showConfirmDialog(this, "CÂU HỎI NÀY SẼ ĐƯỢC CẬP NHẬT VÀO NGÂN HÀNG CÂU HỎI");
            if (input == JOptionPane.YES_OPTION) {
                Questions n = listQ.get(index);
                n.setAa(txtA.getText());
                n.setAb(txtB.getText());
                n.setAc(txtC.getText());
                n.setAd(txtD.getText());
                n.setAnswer((String) cbo.getSelectedItem());
                n.setQ(txtDe.getText());
                if (new QuestionDAO().update(n)) {
                    JOptionPane.showMessageDialog(this, "CẬP NHẬT CÂU HỎI THÀNH CÔNG");
                    listQ.set(index, n);
                    model.setValueAt(n.getQ(), index, 1);
                    model.setValueAt(n.getAa(), index, 2);
                    model.setValueAt(n.getAb(), index, 3);
                    model.setValueAt(n.getAc(), index, 4);
                    model.setValueAt(n.getAd(), index, 5);
                    model.setValueAt(n.getAnswer(), index, 6);
                    this.showDetail();
                } else {
                    JOptionPane.showMessageDialog(this, "CẬP NHẬT CÂU HỎI THẤT BẠI");
                }
            }
        } else {

        }

    }//GEN-LAST:event_btnSuaActionPerformed

