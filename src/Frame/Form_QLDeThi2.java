    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String newId = txt_newE.getText().trim();
        if(newId.length()<1 || newId.length()>10){
            JOptionPane.showMessageDialog(this, "MÃ ĐỀ PHẢI TỪ 1 ĐẾN 10 KÝ TỰ");
            return;
        }else{
            Exam newE = new ExamDAO().findOne(newId);
            if(newE != null){
                JOptionPane.showMessageDialog(this, "ĐỀ THI NÀY ĐÃ CÓ SẴN");
                return;
            }else{
                newE = new Exam();
                newE.setId(newId);
                newE.setStt(true);
                if(new ExamDAO().save(newE)){
                    cbo_examid.addItem(newId);
                    JOptionPane.showMessageDialog(this, "TẠO MỚI ĐỀ THI THÀNH CÔNG");
                }else{
                    JOptionPane.showMessageDialog(this, "TẠO MỚI ĐỀ THI THẤT BẠI");
                }
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

