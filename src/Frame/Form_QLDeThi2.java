    private void btn_deleteQActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteQActionPerformed
        // TODO add your handling code here:
        index = tbl_danhsach.getSelectedRow();
        if(index < 0){
            JOptionPane.showMessageDialog(this, "KHÔNG CÓ GÌ ĐỂ XÓA");
        }else{
            System.out.println(listED.get(index).getQuestions().getQ()+"------"+listED.get(index).getId());
        int input = JOptionPane.showConfirmDialog(this, "CÂU HỎI NÀY SẼ BỊ XÓA ĐỀ");
        if (input == JOptionPane.YES_OPTION) {
            if (listED.size() <= 0) {
                JOptionPane.showMessageDialog(this, "KHÔNG CÓ GÌ ĐỂ XÓA");
                return;
            }
        }
        
        if(new ExamDetailDAO().delete(listED.get(index))){
            listED.remove(index);
            model.removeRow(index);
            if (listED.size() > 0) {
                if (index == listQ.size()) {
                    index--;
                    tbl_danhsach.setRowSelectionInterval(index, index);
                }
            }
            lbl_soluong.setText(""+listED.size());
            JOptionPane.showMessageDialog(this, "ĐÃ XÓA CÂU HỎI KHỎI ĐỀ THI THÀNH CÔNG");
            this.fillToTable();
        }else{
            JOptionPane.showMessageDialog(this, "XÓA THẤT BẠI");
        }
        }
        
    }//GEN-LAST:event_btn_deleteQActionPerformed

