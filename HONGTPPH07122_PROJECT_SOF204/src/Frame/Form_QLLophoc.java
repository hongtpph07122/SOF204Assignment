    public void getcombobox() {
        listClass = new ClassDAO().findAll();
        if (listClass != null) {
            cbo_class.removeAllItems();
            cbo_class2.removeAllItems();
            for (Class c : listClass) {
                cbo_class.addItem(c.getId());
                cbo_class2.addItem(c.getId());
            }
            cbo_class.setSelectedIndex(0);
            cbo_class2.setSelectedIndex(0);
            this.filltotable1();
        } else {
        }
    }

    public void filltotable1() {
        String classID = (String) cbo_class.getSelectedItem();
        if (listAccount != null) {
            listAccount.clear();
        }
        listAccount = new ClassDAO().getStudentList(classID);

        model.setRowCount(0);
        int stt = 1;
        for (Accounts a : listAccount) {
            model.addRow(new Object[]{stt, a.getFullname(), a.getBirthday(), a.isGender() ? "Nam" : "Nữ", a.getPhone()});
            stt++;
        }
        txtsoluong.setText("" + listAccount.size());

    }

    public void filltotable2() {
        if (listANoClass != null) {
            listANoClass.clear();
        }
        listANoClass = new ClassDAO().getStudentList("");
        if (listANoClass != null) {
            model2.setRowCount(0);
            int stt = 1;
            for (Accounts a : listANoClass) {
                model2.addRow(new Object[]{stt, a.getFullname(), a.getBirthday(), a.isGender() ? "Nam" : "Nữ", a.getPhone()});
            }
        }

    }

