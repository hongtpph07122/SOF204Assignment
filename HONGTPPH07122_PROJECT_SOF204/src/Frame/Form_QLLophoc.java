    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Accounts acc = listANoClass.get(index);
        Accounts acc2 = new AccountDAO().findOne(acc.getUsername());
        acc2.setClass_((String) cbo_class2.getSelectedItem());
        if (new AccountDAO().update(acc2)) {
            JOptionPane.showMessageDialog(this, "HỌC SINH ĐÃ ĐƯỢC THÊM VÀO LỚP");
            this.filltotable1();
            listANoClass.remove(index);
            model2.removeRow(index);
            if (listANoClass.size() > 0) {
                if (index == listANoClass.size()) {
                    index--;
                }
                this.showDetail();
            } else {
                txtfullname.setText("NULL");
            }
        } else {
            JOptionPane.showMessageDialog(this, "THÊM VÀO LỚP THẤT BẠI");
        }

    }//GEN-LAST:event_jButton2ActionPerformed


